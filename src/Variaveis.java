public class Variaveis {

    // O método main é o que o Java procura para iniciar a execução do programa
    public static void main(String[] args) {
        // Declaração de variáveis
        // Declarar e inicializar é quando você atribui um valor à variável no momento em que a declara
        int valor;
        int num1, num2, num3;
        int num4, num5 = 4, num6 = 7, num8;
        char caractere;
        float preco = 34.2f; // 'f' indica que é um float, o valor de ponto flutuante padrão é double
        String nome;
        int idade = 25;

        System.out.println("Eu tenho " + idade + " anos.");
    }
}
