public class BibliotecaMath {
    public static void main(String[] args) {
        System.out.println("O valor de PI é: " + Math.PI);
        System.out.println("O valor arredondado de PI é: " + Math.round(Math.PI));
        System.out.println("O valor de E é: " + Math.E);
        System.out.println("O valor absoluto de -10 é: " + Math.abs(-10));
        System.out.println("O coseno de 45 graus é: " + Math.cos(45));
        System.out.println("O valor de 2 elevado a 3 é: " + Math.pow(2, 3));
        System.out.println("A raiz quadrada de 16 é: " + Math.round(Math.sqrt(16)));
    }
}
